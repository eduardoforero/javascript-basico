// var

/* Puede redeclararse y reasignarse 
   tiene solo scope global o de función
   permite un hoisting de iniciación a undefined
*/

var x = ""

// let

/* No pueden redeclararse, pero sí reasignarse
   Tienen scope global, de función o de bloque
   en hoisting no se inicializa a ningún valor
*/

let y = ""

// const

/* No puede redeclararse ni reasignarse
   tiene scope global, de función o de bloque
   en hoisting no se inicializa a ningún valor
*/

const z = ""